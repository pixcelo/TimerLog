CREATE TABLE T_TimerLog (
    Id BIGINT IDENTITY(1,1) NOT NULL,
    StartAt DATETIME NOT NULL,
    EndAt DATETIME NOT NULL,
    UserId BIGINT NOT NULL,
    TypeId BIGINT NULL,
    CONSTRAINT PK_T_TimerLog PRIMARY KEY CLUSTERED 
(
	Id ASC
));

GO

